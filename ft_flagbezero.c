#include "ft_printf.h"

void	ft_fbezero(t_flag *f)
{
	f->left = 0;
	f->space = 0;
	f->sign = 0;
	f->zero = 0;
	f->prec = 0;
	f->len = 0;
	f->flong = 0;
	f->h = 0;
	f->z = 0;
	f->j = 0;
	f->sharp = 0;
	f->nonprint = 0;
}
