#include "ft_printf.h"

void	ft_fcheck(t_flag *f)
{
	long i;

	i = 0;
	if (f->flong != 0)
		i++;
	if (f->h != 0)
		i++;
	if (f->z != 0)
		i++;
	if (f->j != 0)
		i++;
	if (i > 1)
	{
		f->flong = 0;
		f->h = 0;
		f->z = 0;
		f->j = 1;
	}
}
